function id(prefix, name) {
  return `demo_${prefix}_${name.toLowerCase().replace(/[^a-z0-9]+/g, "_").replace(/^_+|_+$/g, "")}`;
}

function money(value) {
  return Number(value || 0);
}

function item(productId, quantity, unitPrice, extra = {}) {
  return {
    productId,
    itemName: extra.itemName || "",
    itemType: extra.itemType || "",
    sku: extra.sku || "",
    unitOfMeasure: extra.unitOfMeasure || "piece",
    description: extra.description || "",
    quantity,
    unitPrice,
    lineTotal: quantity * unitPrice
  };
}

async function insertMany(db, collectionName, rows) {
  if (!rows.length) return;
  await db.collection(collectionName).insertMany(rows.map((row) => ({ ...row, isDemo: true })), { ordered: false });
}

